    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // Adding functionality to the add button in addcustomer option
            String FirstName=firstname.getText();
            String LastName=lastname.getText();
            String PassportID=passport.getText();
            String CustomerID=customerID.getText();
            String NicID=nic.getText();
            String Address=address.getText();
            DateFormat df=new SimpleDateFormat("DD-MM-YYYY"); // We have to set a format for date because date is in a form of JDate and we want date in string form
            String DOB=df.format(dob.getDate()); // We are simply typecasting the date format
            String Gender;
            if(male.isSelected()){
                Gender="Male";
            }
            else{
                Gender="Female";
            }
            String Contact=contact.getText();
            Class.forName("com.mysql.jdbc.Driver");
            connection=DriverManager.getConnection("jdbc:mysql://localhost/airlinemanagementdb","root","root@mysql");
            pre=connection.prepareStatement("insert into customer(CustomerID,FirstName,LastName,PassportID,NicID,Address,DOB,Gender,Contact)values(?,?,?,?,?,?,?,?,?)");
            pre.setString(1,CustomerID);
            pre.setString(2,FirstName);
            pre.setString(3,LastName);
            pre.setString(4,PassportID);
            pre.setString(5,NicID);
            pre.setString(6,Address);
            pre.setString(7,DOB);
            pre.setString(8,Gender);
            pre.setString(9,Contact);
            pre.executeUpdate(); // Just updating the values
            JOptionPane.showMessageDialog(null,"Customer Added Successfully ✌️");
            
            AutoID(); // Calling autoid function to generate new id for next new customer addition
            
            // Setting values of all field to null 
            firstname.setText("");
            lastname.setText("");
            passport.setText("");
            customerID.setText("");
            nic.setText("");
            address.setText("");
            dob.setDate(null);
            if(male.isSelected()){
                male.setSelected(false);
            }
            else{
                female.setSelected(false);
            }
            contact.setText("");
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


