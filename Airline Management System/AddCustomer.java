    public void AutoID(){
        try {
            Class.forName("com.mysql.jdbc.Driver");
            connection=DriverManager.getConnection("jdbc:mysql://localhost/airlinemanagementdb","root","root@mysql");
            pre=connection.prepareStatement("Select max(CustomerID) from customer");
            ResultSet rs=pre.executeQuery();
            rs.next(); //Pointing to next entry
            if(rs.getString("max(CustomerID)")==null){
                customerID.setText("CS001");
            }
            else{
                int id=Integer.parseInt(rs.getString("max(customerID)").substring(2,rs.getString("max(customerID)").length()));
                id++;
                customerID.setText("CS"+String.format("%03d", id));
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
